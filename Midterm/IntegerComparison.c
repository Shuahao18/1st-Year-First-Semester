#include <stdio.h>
#include <stdlib.h>

int main ()
{
    int A = 9;
    int B = 9;


    if(A>B)
    {
    printf("A is greater than B \n");
    }

    if(A<B)
    {
    printf("A is less than B \n");
    }

    if(A==B)
    {
    printf("A and B are both equals \n");
    }

    return 0;

}


